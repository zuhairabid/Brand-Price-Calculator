function followerToPrice(fol, platform_id) {
   let tiktok_keys
   let platform
   if (platform_id == 0) {
      platform = tiktok
      tiktok_keys = Object.keys(platform)
   } else if (platform_id == 1) {
      platform = youtube
      tiktok_keys = Object.keys(platform)
   } else if (platform_id == 2) {
      platform = instagram
      tiktok_keys = Object.keys(platform)
   }
   const price = tiktok_keys.filter((i) => {
      return i <= fol
   })
   return platform[price[price.length - 1]]
}
